def wait_and_check(max_attempts=10, delay=3):
    """–û–∂–∏–¥–∞–µ—Ç –≥–æ—Ç–æ–≤–Ω–æ—Å—Ç–∏ PostgreSQL —Å –Ω–µ—Å–∫–æ–ª—å–∫–∏–º–∏ –ø–æ–ø—ã—Ç–∫–∞–º–∏"""
    
    print(f"üîÑ –ù–∞—á–∏–Ω–∞–µ–º –ø—Ä–æ–≤–µ—Ä–∫—É —Å {max_attempts} –ø–æ–ø—ã—Ç–∫–∞–º–∏ (–∏–Ω—Ç–µ—Ä–≤–∞–ª {delay}—Å)")
    
    for attempt in range(1, max_attempts + 1):
        print(f"\n--- –ü–æ–ø—ã—Ç–∫–∞ {attempt}/{max_attempts} ---")
        
        if check_connection():
            return True
            
        if attempt < max_attempts:
            print(f"‚è≥ –û–∂–∏–¥–∞–µ–º {delay} —Å–µ–∫—É–Ω–¥ –¥–æ —Å–ª–µ–¥—É—é—â–µ–π –ø–æ–ø—ã—Ç–∫–∏...")
            time.sleep(delay)
    
    print(f"\nüí• –ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–¥–∫–ª—é—á–∏—Ç—å—Å—è –ø–æ—Å–ª–µ {max_attempts} –ø–æ–ø—ã—Ç–æ–∫")
    return False

if __name__ == "__main__":
    print("=== –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ PostgreSQL ===")
    
    if len(sys.argv) > 1 and sys.argv[1] == '--wait':
        success = wait_and_check()
    else:
        success = check_connection()
    
    sys.exit(0 if success else 1) 